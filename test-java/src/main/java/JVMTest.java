    public static long ackermann(long m, long n) {
        if (m == 0) return n + 1;
        if (n == 0) return ackermann(m - 1, 1);
        return ackermann(m - 1, ackermann(m, n - 1));
    }

    public static int[] quicksort(int arr[], int l, int r) {
        int q;
        if (l < r) {
            q = partition(arr, l, r);
            quicksort(arr, l, q);
            quicksort(arr, q + 1, r);
        }
        return arr;
    }

    static int partition(int arr[], int l, int r) {
        int i, j, x = arr[(l + r) / 2];
        i = l - 1;
        j = r + 1;
        while (true) {
            do {
                ++i;
            } while (arr[i] < x);

            do {
                --j;
            } while (arr[j] > x);

            if (i < j) {
                int k = arr[i];
                arr[i] = arr[j];
                arr[j] = k;
            } else {
                return j;
            }
        }
    }

